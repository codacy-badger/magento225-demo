<?php

namespace Srtpl\Testcasedemo\Model;

class Calculator {
    /**
     * this function will perform the addition of two numbers
     *
     * @param float $a
     * @param float $b
     * @return float
     */
    Public function addition($a ,$b) {
        return $a + $b;
    }

    /**
     * this function will perform the addition of two numbers
     *
     * @param float $a
     * @param float $b
     * @return float
     */
    Public function substract($a ,$b) {
        return $a - $b;
    }
}